# Definí una variable con un float
var = 12.3
# Definí una variable con texto
var2 = "segundo variable"
# Definí una variable boolean
var3 = true
# Cuento con variables
sum = var + 34.9
# Y, a nuevo una variable con texto
text = "texto"

